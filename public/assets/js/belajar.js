function toggleFormDone1() {
    var feedbackDiv = document.querySelector(".feedback1");
    if (feedbackDiv.style.display === "none") {
      feedbackDiv.style.display = "block";
    } else {
      feedbackDiv.style.display = "none";
    }
  }

